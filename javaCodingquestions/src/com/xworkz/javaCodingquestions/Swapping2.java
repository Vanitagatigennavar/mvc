public class Swapping2 {

	public static void main(String[] args) {
		int x=150; int y=250;
		System.out.println("before swapp");
		System.out.println(x);
		System.out.println(y);
		
		 int temp=x;
		 x=y;
		 y=temp;
System.out.println("after swapping");
System.out.println(x);
System.out.println(y);
	}

}
